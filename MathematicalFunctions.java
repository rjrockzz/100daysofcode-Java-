public class MathematicalFunctions {
    public static void main(String[] args) {
        double a = Math.cos(90);
        System.out.printf("%2.3f\n",a);
        double b = Math.sin(90);
        System.out.printf("%2.3f\n",b);
        double c = Math.tan(90);
        System.out.printf("%2.3f\n",c);
        double d = Math.acos(0.5);
        System.out.printf("%2.3f\n",d);
        double f = Math.asin(0.5);
        System.out.printf("%2.3f\n",f);
        double g = Math.atan(0);
        System.out.printf("%2.3f\n",g);
        double h = Math.atan2(1,2);
        System.out.printf("%2.3f\n",h);
    }
}
