public class MathematicalFunctions2 {
    public static void main(String[] args) {
        double a = Math.log(10);
        System.out.printf("%2.3f\n", a);
        double b = Math.log10(10);
        System.out.printf("%2.3f\n", b);
        double c = Math.cbrt(8);
        System.out.printf("%2.3f\n", c);
        double d = Math.pow(2,3);
        System.out.printf("%2.3f\n", d);
        double f = Math.abs(34.222);
        System.out.printf("%2.3f\n", f);
        double g = Math.floor(23.4);
        System.out.printf("%2.3f\n", g);
        double h = Math.ceil(23.33);
        System.out.printf("%2.3f\n",h);
        int i = Math.max(34,32);
        System.out.println(i);
    }
}
