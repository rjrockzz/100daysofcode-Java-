public class typecast {
    public static void main(String[] args) {
        double d = 45.344539993828821;
        float a = (float) d;
        System.out.println(a);
        int x = 86;
        char c = (char) x;
        System.out.println(c);
        char z = '\u0076';
        short f = (short) z;
        System.out.println(f);
    }
}
